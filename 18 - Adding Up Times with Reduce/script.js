// =============================================================================
// 取得總秒數
// =============================================================================
let li = document.querySelectorAll('li');
let d = [...li] // 轉陣列
    .map(el => el.dataset.time)
    .map(time => {
        let [min, sec] = time.split(':');
        return min * 60 + sec * 1;
    })
    .reduce((prev, current) => prev + current);

console.log(d); // 總秒數

